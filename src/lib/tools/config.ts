export interface CalcRef {
  slug: string;
  categorySlug: string;
}

export interface ToolFaqItem {
  question: string;
  answer: string;
}

export interface ToolConfig {
  slug: string;
  /** UI-заголовок страницы инструмента (отображается на самой странице). */
  title: string;
  /**
   * SEO-title для <head> по единой системе сайта (без « — Мастерок»,
   * суффикс добавит layout template). Если не задан — собирается из title.
   * Формат: «[Название инструмента] онлайн».
   */
  seoTitle?: string;
  /** Короткое имя для карточки на хабе */
  cardTitle: string;
  description: string;
  cardDescription: string;
  priority: number;
  icon: string;
  badge: string;
  color: string;
  bg: string;
  relatedCalculators: CalcRef[];
  faq: ToolFaqItem[];
  seoIntro: string;
  /** Не индексировать (утилита общего назначения) */
  noindex?: boolean;
}

export function calcHref(ref: CalcRef): string {
  return `/kalkulyatory/${ref.categorySlug}/${ref.slug}/`;
}

export function toolHref(slug: string): string {
  return `/instrumenty/${slug}/`;
}

