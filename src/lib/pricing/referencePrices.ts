/**
 * Возвращает копию справочных цен. Не мутабельный — пользователь
 * может менять цены после подстановки, оригинал не трогается.
 */
export function getReferenceMaterialPrices(): Record<string, number> {
  return { ...REFERENCE_MATERIAL_PRICES };
}

/**
 * Подбирает справочную цену для материала по его price basis key.
 * Ищет точное совпадение, затем частичное (по началу имени).
 */
export function getReferencePrice(basisKey: string): number | undefined {
  // Точное совпадение
  if (basisKey in REFERENCE_MATERIAL_PRICES) {
    return REFERENCE_MATERIAL_PRICES[basisKey];
  }

  // Частичное совпадение по префиксу имени материала
  const namePart = basisKey.split("|")[0].toLowerCase();
  for (const [key, price] of Object.entries(REFERENCE_MATERIAL_PRICES)) {
    const keyName = key.split("|")[0].toLowerCase();
    if (keyName.includes(namePart) || namePart.includes(keyName)) {
      return price;
    }
  }

  return undefined;
}
